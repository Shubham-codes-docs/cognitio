//class collection
const branding = document.querySelector(".tm-brand-name");
const para = document.querySelector(".tm-hero-text");
const eventHeads = document.querySelector(".event-heads");
const rulesHeading = document.querySelector(".rules");
const rules1 = document.querySelector(".rules-sec-1");
const rules2 = document.querySelector(".rules-sec-2");
const eventImage = document.querySelector(".event-image");

//data adding function

const hydrateData = (event) => {
  branding.textContent = event.name;
  eventImage.src = event.img;
  para.textContent = event.desc;
  if (event.details1) {
    event.details1.forEach((detail) => {
      let li = document.createElement("li");
      li.textContent = detail;
      rules1.appendChild(li);
    });
  }

  if (event.details2) {
    event.details2.forEach((detail) => {
      let lio = document.createElement("li");
      let iul = document.createElement("ul");
      lio.textContent = detail.heading;
      detail.points.forEach((point) => {
        let lii = document.createElement("li");
        lii.textContent = point;
        iul.appendChild(lii);
      });
      lio.appendChild(iul);
      rules2.appendChild(lio);
    });
  }

  event.eh.forEach((head) => {
    let li = document.createElement("li");
    li.textContent = head;
    eventHeads.appendChild(li);
  });
};

